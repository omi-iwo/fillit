#include "fillit.h"

void	place(char *test, int **figures)
{
	int	i;
	int	j;
	int	h;
	int	w;

	i = 0;
	j = 20;
	h = 0;
	w = 0;
	while (test[i] != '\0')
	{
		if (test[i] == '#')
		{
			figures[h][w++] = (((i - (j - 20)) + 1) % 5);
			figures[h][w++] = (((i - (j - 20)) + 1) / 5) + 1;
		}
		if (i++ == j)
		{
			j += 21;
			w = 0;
			h++;
		}
	}
	free(test);
	return ;
}
